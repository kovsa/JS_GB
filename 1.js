// 4. *Самостоятельно разобраться с атрибутами тега script (async и defer)
// Атрибут defer сообщает браузеру, что он должен продолжать обрабатывать страницу и загружать скрипт в фоновом режиме, а затем запустить этот скрипт, когда он загрузится. Отложенные с помощью defer скрипты сохраняют порядок относительно друг друга, как и обычные скрипты. Поэтому, если сначала загружается большой скрипт, а затем меньшего размера, то последний будет ждать.
// Атрибут async означает, что скрипт абсолютно независим: Содержимое страницы отображается сразу же : async его не блокирует. DOMContentLoaded может произойти как до, так и после async, никаких гарантий нет. Асинхронные скрипты не ждут друг друга. Cкрипты выполняются в порядке загрузки.