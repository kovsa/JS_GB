// 1. Задать температуру в градусах по Цельсию.
// Вывести в alert соответствующую температуру в градусах по Фаренгейту.
// Подсказка: расчёт идёт по формуле: Tf = (9 / 5) * Tc + 32, 
// где Tf – температура по Фаренгейту, Tc – температура по Цельсию
var Tc = parseFloat(prompt("Введите температуру по Цельсию"));
var Tf = (9 / 5) * Tc + 32;
alert(`Температура по Фаренгейту: ${Tf}`);


// 2. Работа с переменными. Объявить две переменные: admin и name. 
// Записать в name строку "Василий"; 
// Скопировать значение из name в admin. 
// Вывести admin (должно вывести «Василий»).

var name = "Василий";
var admin = name;
alert(admin);


